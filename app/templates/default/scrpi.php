        <h3>Supplementary Analysis – Naming Coffee Growers</h3>
        <p class="light-font-smaller">While collecting 2015, Q2 SCRPI price data, we documented whether the name of the producing farm, farmer or cooperative was specifically mentioned on the web page that marketed each coffee. For example, when Blue Bottle Coffee sold its Brazil FAF Santa Clara Red Catuai Natural coffee, customers were told that “in the highlands of Divinolandia, Brazil, Milton Nogueira, the esteemed producer of Santa Clara estate, cares for his red Catuai coffee variety trees with a lot of pride.”</p>
        <p class="light-font-smaller">How prevalent is this practice of naming coffee growers? More than 60% (36 out of 58) of the highest-priced coffee listings in the 2015, Q2 SCRPI mentioned coffee growers by name. This percentage was just 12% (7 out of 58) for the lowest-priced coffee listings. The difference is explained in part by the fact that 44 out of the 58 lowest-priced coffees are blends and espressos, compared to only nine of the 58 highest-priced coffees.</p>
        <p class="light-font-smaller">This suggests that a non-trivial number of coffee growers are being recognized for the work that they do, and that this practice is more common among the more highly-valued coffees.</p>
        <p class="light-font-smaller">Does the specialty coffee market value knowing the identity of the coffee grower? The following chart suggest that it does, but only at the high end of the market. Among the lowest-priced coffees in the 2015, Q2 SCRPI, the seven that made specific reference to the grower sold for an average of $16.25 per pound, a $0.11 (0.7%) premium over the other lowest-priced coffees. Among the highest-priced coffees, the 36 that specifically named the grower sold for an average of $29.99 per pound; $5.98 (24.9%) more than the average charged for the other highest-priced coffees.</p>
