        <p>In the first quarter of 2015, the SCRPI rose by 6.2%. Overall, the average price charged for roasted coffee at the end of March, 2015 was $22.01 per pound. This breaks down into an average of $16.51 per pound for the lowest-priced coffees (+0.2%), and an average of $27.52 for the highest-priced coffees (+10.1%) sold on the websites of the Index roasters.**</p>
        <p>While retail coffee prices continued to rise through the first quarter of 2015, the corresponding ICO Composite Price for green coffee fell to $1.27 per pound. With the steady decline since September, 2014, the ICO price is currently just 7.2% of the overall SCRPI average.</p>
