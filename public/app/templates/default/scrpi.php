            <p>* To generate the SCRPI, we track a roster of “blue chip” coffee roasters; those that set standards in the North American specialty coffee market. We focus on roasters whose coffees receive the lion’s share of attention from Coffee Review, and those who receive many of the Good Food Awards and Roast Magazine Awards. To capture both ends of the specialty coffee market, we visit the website of each Index roaster quarterly and record information about their lowest and highest-priced coffees; ignoring the very high-priced Blue Mountain, Kona, Geisha/Esmeralda and Civet/Kopi Luwak coffees. In September, 2014, the number of SCRPI roasters fell from 60 to 59, as Green Mountain Coffee was acquired by Keurig, which does not sell roasted coffee in bags. In March, 2015, the number was 58 because the Supreme Bean website is under construction.</p>
